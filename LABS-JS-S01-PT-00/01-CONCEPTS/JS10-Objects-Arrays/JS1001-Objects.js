// Objects
// properties: state
// methods: behavior

// Literal Object
var personOne = {
    name: "Joshua",
    age: 30,

    sayHello: function() {}
};
console.log( personOne );

// Intance class
var personTwo = new Object();
personTwo.name = "John";
personTwo.age = 27;
personTwo.sayHello = function() {};
console.log( personTwo );
